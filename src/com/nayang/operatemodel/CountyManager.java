package com.nayang.operatemodel;

public class CountyManager {

	private int m_countyId;
	private String m_countyName;
	private String m_countyCode;
	private int m_cityId;
	
	public int getM_countyId() {
		return m_countyId;
	}
	public void setM_countyId(int m_countyId) {
		this.m_countyId = m_countyId;
	}
	public String getM_countyName() {
		return m_countyName;
	}
	public void setM_countyName(String m_countyName) {
		this.m_countyName = m_countyName;
	}
	public String getM_countyCode() {
		return m_countyCode;
	}
	public void setM_countyCode(String m_countyCode) {
		this.m_countyCode = m_countyCode;
	}
	public int getM_cityId() {
		return m_cityId;
	}
	public void setM_cityId(int m_cityId) {
		this.m_cityId = m_cityId;
	}
	
	
	
}
