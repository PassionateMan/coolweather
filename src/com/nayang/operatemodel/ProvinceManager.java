package com.nayang.operatemodel;

public class ProvinceManager {
	
	private int m_provinceId;
	private String m_provinceName;
	private String m_provinceCode;
	
	public int getM_provinceId() {
		return m_provinceId;
	}
	public void setM_provinceId(int m_provinceId) {
		this.m_provinceId = m_provinceId;
	}
	public String getM_provinceName() {
		return m_provinceName;
	}
	public void setM_provinceName(String m_provinceName) {
		this.m_provinceName = m_provinceName;
	}
	public String getM_provinceCode() {
		return m_provinceCode;
	}
	public void setM_provinceCode(String m_provinceCode) {
		this.m_provinceCode = m_provinceCode;
	}
	

}
